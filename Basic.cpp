#include <iostream>
using namespace std;

int main() {
	// varible is a box
	int a; // declare a varible
	// initialize
	a = 28; // 二进制，八进制，十进制，十六进制 10(D)->1010(B)
	cout<<a<<endl; // endl='\n'
	/*
		常用存存储单位：
		比特  bit（b） 1位二进制（0|1）
		字节  byte（B） 8位二进制 —> 1B = 8b
		千字节 Kilo-byte（KB）   1024*8位二进制 1KB=1024B
		1MB = 1024KB
		1GB = 1024MB
		1TB = 1024GB
	*/
	return 0;
}